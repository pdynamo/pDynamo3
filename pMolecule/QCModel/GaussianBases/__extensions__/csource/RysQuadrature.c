/*----------------------------------------------------------------------------------------------------------------------------------
! . Evaluate the Rys quadrature roots.
!---------------------------------------------------------------------------------------------------------------------------------*/
void RysQuadrature_Roots    ( RysQuadrature *roots, Integer nRoots, Real x )
{
   switch ( nRoots )
   {
      case 1: Roots1     ( roots,         x ) ; break ;
      case 2: Roots2     ( roots,         x ) ; break ;
      case 3: Roots3     ( roots,         x ) ; break ;
      case 4: Roots4     ( roots,         x ) ; break ;
      case 5: Roots5     ( roots,         x ) ; break ;
      case 6: Roots6Plus ( roots, nRoots, x ) ; break ;
   }
}

