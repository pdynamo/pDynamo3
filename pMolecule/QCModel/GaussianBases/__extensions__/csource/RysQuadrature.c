/*----------------------------------------------------------------------------------------------------------------------------------
! . Find the ith root (rt[i]) of a polynomial of order k whose mth coefficient is stored in a[m].
! . The initial values in rt bracket the final values.
!---------------------------------------------------------------------------------------------------------------------------------*/
# define TOLERANCE 1.0e-9
static Boolean RysFindRoot ( const Real *a, Real *rt, const Integer  k )
{
    Integer  i, m ;
    Real     dr, p1, p2, p3, p4, p5, p6, r, r1, r2, r3, r4, r5, r6 ;
    /* . Initialization. */
    r2 = 0.0e+00 ;
    p2 = a[0] ;
    /* . Loop over order of the polynomial. */
    for ( m = 0 ; m < k ; m++ )
    {
        r1 = r2 ;
        p1 = p2 ;
        r2 = rt[m] ;
        p2 = a[k] ;
        for ( i = 1 ; i <= k ; i++ ) p2 = p2 * r2 + a[k-i] ;
        if ( p1 * p2 >= 0.0e+00 )
        {
            printf ( "\nRys Quadrature Error> Root number %3d was not found for polynomial of order %3d\n", m, k ) ;
            return False ;
        }
        r5 = r1 ;
        p5 = p1 ;
        r6 = r2 ;
        p6 = p2 ;
        while ( True )
        {
            r3 = r5 ;
            p3 = p5 ;
            r4 = r6 ;
            p4 = p6 ;
            r  = ( r3 * p4 - r4 * p3 ) / ( p4 - p3 ) ;
            dr = r4 - r3 ;
            if ( fabs ( dr ) < TOLERANCE )
            {
                rt[m] = r ;
                break ;
            }
            dr *= 0.0625e+00 ;
            r5  = r - dr ;
            if ( r5 < r3 ) r5 = r3 ;
            r6  = r + dr ;
            if ( r6 > r4 ) r6 = r4 ;
            p5 = a[k] ;
            p6 = p5 ;
            for ( i = 1 ; i <= k ; i++ )
            {
                p5 = p5 * r5 + a[k-i] ;
                p6 = p6 * r6 + a[k-i] ;
            }
            while ( p5 * p6 >= 0.0e+00 )
            {

                if ( p3 * p5 <= 0.0e+00 )
                {
                    r5 = 0.25e+00 * r3 + 0.75e+00 * r5 ;
                    p5 = a[k] ;
                    for ( i = 1 ; i <= k ; i++ ) p5 = p5 * r5 + a[k-i] ;
                }
                else
                {
                    r6 = 0.25e+00 * r4 + 0.75e+00 * r6 ;
                    p6 = a[k] ;
                    for ( i = 1 ; i <= k ; i++ ) p6 = p6 * r6 + a[k-i] ;
                }
            }
        }
    }
    return True ;
}
# undef TOLERANCE

/*----------------------------------------------------------------------------------------------------------------------------------
! . Rys function.
!---------------------------------------------------------------------------------------------------------------------------------*/
# define SMALL 0.5409855304296342219319112e-78
# define TOLERANCE 1.0e-12
static void RysFunction ( const Real x, const Integer  n, Real *ff )
{
    Integer  m ;
    Real     a, e, fac, facmin, sum, term, tmax, xx ;
    /* . Initialization. */
    e  = SMALL ;
    xx = x + x ;
    facmin = xx ;
    if ( facmin < 2 * 180.2160e+00 ) e = exp ( - x ) ;
    /* . Small arguments. */
    if ( facmin <= 80.0e+00 )
    {
        term = 1.0e+00 ;
        sum  = 1.0e+00 ;
        fac  = ( Real ) n ;
        fac += 0.5e+00 ;
        do
        {
            fac  += 1.0e+00 ;
            term *= x / fac ;
            sum  += term ;
        }
        while ( ( fac <= facmin ) || ( term > sum * TOLERANCE ) ) ;
        fac   = ( Real ) ( n + n + 1 ) ;
        ff[n] = sum * e / fac ;
        m     = n - 1 ;
        fac   = ( Real ) ( m + m + 1 ) ;
        while ( m >= 0 )
        {
            ff[m] = ( e + xx * ff[m+1] ) / fac ;
            m    -= 1 ;
            fac  -= 2.0e+00 ;
        }
    }
    /* . Large arguments - asymptotic expansion. */
    else
    {
        a    = sqrt ( 0.7853981633974483096156608e+00 / x ) ;
        tmax = a * TOLERANCE / e ;
        term = 1.0e+00 / xx ;
        sum  = term ;
        fac  = 1.0e+00 ;
        do
        {
            fac  -= 2.0e+00 ;
            term *= fac / xx ;
            sum  += term ;
        }
        while ( fabs ( term ) > tmax ) ;
        ff[0] = a - e * sum ;
        fac   = -1.0e+00 ;
        m     = 0 ;
        while ( m < n )
        {
            m   += 1 ;
            fac += 2.0e+00 ;
            ff[m] = ( fac * ff[m-1] - e ) / xx ;
        }
    }
}
# undef SMALL
# undef TOLERANCE

/*----------------------------------------------------------------------------------------------------------------------------------
! .  Calculate an N by N triangular matrix C such that C^T S C = I.
!---------------------------------------------------------------------------------------------------------------------------------*/
# define TOLERANCE 1.0e-80 /* . This is about the minimum - any smaller and there are problems. */
static void RysSymmetricMatrix ( Real c[][_MAXRYS+1], Real s[][_MAXRYS+1], const Integer  n )
{
    Integer  i, j, k ;
    Real     dot, fac, v[_MAXRYS+1], y[_MAXRYS+1] ;
    /* . Initialization. */
    for ( i = 0 ; i < n ; i++ )
    {
        for ( j = 0 ; j < n ; j++ ) c[i][j] = 0.0e+00 ;
    }
    /* . Calculate C. */
    for ( j = 0 ; j < n ; j++ )
    {
        fac = s[j][j] ;
        for ( k = 0 ; k < j ; k++ )
        {
            v[k] = 0.0e+00 ;
            y[k] = s[k][j] ;
        }
        for ( k = 0 ; k < j ; k++ )
        {
            for ( i = 0, dot = 0.0e+00 ; i <= k ; i++ ) dot  += c[i][k] * y[i] ;
            for ( i = 0                ; i <= k ; i++ ) v[i] -= c[i][k] * dot  ;
            fac -= dot * dot ;
        }
        if ( fabs ( fac ) > TOLERANCE ) fac = 1.0e+00 /  sqrt ( fac ) ;
        else                            fac = 0.0e+00 ;
        c[j][j] = fac ;
        for ( k = 0 ; k < j ; k++ ) c[k][j] = fac * v[k] ;
    }
}
# undef TOLERANCE
# endif /* . Rys method. */

