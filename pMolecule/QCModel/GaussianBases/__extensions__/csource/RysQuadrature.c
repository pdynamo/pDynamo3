/*----------------------------------------------------------------------------------------------------------------------------------
! . More than five roots.
! . This version uses Christoffel formula for weights with the ith root of the jth polynomial in r[i][j] and its weight in w[i][j].
! . This can fail for high yy and should be replaced with a numerically more stable version (particularly finding the C matrix and
! . locating the roots).
!---------------------------------------------------------------------------------------------------------------------------------*/
# define TOLERANCE 1.0e-300
static void Roots6Plus ( RysQuadrature *roots, Integer  mRoots, Real yy )
{
    Boolean  QSUCCESS = True ;
    Integer  i, j, k, m, n, nn, n1 ;
    Real     dum, poly, root, wsum, x ;
    Real     a[_MAXRYS+1], c[_MAXRYS+1][_MAXRYS+1], ff[2*(_MAXRYS+1)], r[_MAXRYS][_MAXRYS],
             rt[_MAXRYS+1], s[_MAXRYS+1][_MAXRYS+1], w[_MAXRYS][_MAXRYS] ;
    /* . Initialization. */
    n  = Maximum ( mRoots, 2 ) ;
    x  = yy ;
    n1 = n + 1 ;
    nn = n + n ;
    RysFunction ( x, nn, ff ) ;
    for ( i = 0 ; i < n1 ; i++ )
    {
        for ( j = 0 ; j < n1 ; j++ ) s[i][j] = ff[i+j] ;
    }
    RysSymmetricMatrix ( c, s, n1 ) ;
    for ( i = 0 ; i < n ; i++ )
    {
        for ( j = 0 ; j < n ; j++ ) { r[i][j] = 0.0e+00 ; w[i][j] = 0.0e+00 ; }
    }
    /* . Calculation. */
    wsum    = ff[0] ;
    w[0][0] = wsum  ;
    if ( fabs ( wsum ) > TOLERANCE ) r[0][0] = ff[1] / wsum ;
    else                             r[0][0] = 0.0e+00 ;
    dum     = sqrt ( pow ( c[1][2], 2 ) - 4.0e+00 * c[0][2] * c[2][2] ) ;
    r[0][1] = 0.5e+00 * ( -c[1][2] - dum ) / c[2][2] ;
    r[1][1] = 0.5e+00 * ( -c[1][2] + dum ) / c[2][2] ;
    if ( n > 2 )
    {
        for ( i = 2 ; i < n1 ; i++ ) rt[i] = 1.0e+00 ;
        rt[0] = r[0][1] ;
        rt[1] = r[1][1] ;
        for ( k = 3 ; k <= n ; k++ )
        {
            for ( i = 0 ; i <= k ; i++ ) a[i] = c[i][k] ;
            QSUCCESS = RysFindRoot ( a, rt, k ) ;
            if ( ! QSUCCESS ) return ;
            for ( i = 0 ; i < k ; i++ ) r[i][k-1] = rt[i] ;
        }
    }
    for ( k = 1 ; k < n ; k++ )
    {
        for ( i = 0 ; i <= k ; i++ )
        {
            root = r[i][k] ;
            dum  = 1.0e+00 / ff[0] ;
            for ( j = 0 ; j < k ; j++ )
            {
                poly = c[j+1][j+1] ;
                for ( m = 0 ; m <= j ; m++ ) poly = poly * root + c[j-m][j+1] ;
                dum += poly * poly ;
            }
            w[i][k] = 1.0e+00 / dum ;
        }
    }
    for ( k = 0 ; k < mRoots ; k++ )
    {
        dum = r[k][mRoots-1] ;
        roots->roots[k]   = dum / ( 1.0e+00 - dum ) ;
        roots->weights[k] = w[k][mRoots-1] ;
    }
}
